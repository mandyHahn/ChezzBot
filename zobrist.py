def computeHash(board, turn):
    h = 0
    for l, v in board.items():
        h ^= Z_VALUES[l[0]][l[1]][PIECE_LOCATIONS[v]]
        
    if turn == "b":
        h ^= Z_B_TURN
        
    return h

